let money = 100.50;
let price = 105.50;

if (money > price) {  
    console.log("You paid extra, here's your change."); // A
} else if (money === price) {  
    console.log("You paid the exact amount, have a nice day!"); // B
} else {  
    console.log("That's not enough, you still owe me money."); // C
}


