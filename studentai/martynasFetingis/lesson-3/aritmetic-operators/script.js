// 1st function - SUM //

function sum(a, b) {
  return a + b;
}
console.log(sum(5, 7));

// 2nd function - SUBTRACTION //

function sub(a) {
  return 100 - a;
}
console.log(sub(55));

// 3rd function - MULTIPLICATION //

function multi(a, b) {
  return a * b;
}
console.log(multi(10, 5));

// 4th function - Exponential //

function expo(a, b) {
  return a ** b;
}
console.log(expo(10, 3));

// 5th function - Division //

function div(a, b) {
  return a / b;
}
console.log(div(10, 5));

// 6th function - Modulus(remainder) //

function mod(a, b) {
  return a % b;
}
console.log(mod(10, 3));

// 7th function - increment //

function increment(a) {
  return ++a;
}
console.log(increment(1));

// 8th function - decrement //
function decrement(a) {
  return --a;
}
console.log(decrement(10));
