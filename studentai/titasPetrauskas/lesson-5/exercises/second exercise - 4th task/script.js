let groupMembers = 5;

if (groupMembers <= 0) {
  console.log('not a group');
} else if (groupMembers == 1) {
  console.log('solo');
} else if (groupMembers == 2) {
  console.log('duo');
} else if (groupMembers == 3) {
  console.log('trio');
} else if (groupMembers == 4) {
  console.log('quatro');
} else console.log('large group');
